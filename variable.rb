# 変数は、文字列などのオブジェクトを作成したときに、そのオブジェクトにつける名札

webcamp = "プログラミング学習"
puts webcamp
  # 「puts webcamp」により、変数webcampに入ったデータが出力されました。
  # このように、「変数名 = 入れたいデータ」とすることで変数を作成できる。
  # この場合の「=」は、「右のオブジェクトを左の変数に代入する」という意味。

webcamp = "オンラインプログラミング学習"
puts webcamp

# 実行結果　プログラミング学習　　　　　：１つ目のputsの出力結果
# 　　　　　オンラインプログラミング学習：２つ目のputsの出力結果
  # 変数webcampに再度値を代入したため、２つ目の結果はそれに代わっている。
  # このように、変数の値は書き換えることができる。