  # 　　　　　　数字を入力してください。
  # 　　　　　　5
  # 　　　　　　結果は...
  # 　　　　　　Buzz
  # 実行結果　３と５の両方で割り切れる数値が入力された場合
  # 　　　　　　数字を入力してください。
  # 　　　　　　15
  # 　　　　　　結果は...
  # 　　　　　　FizzBuzz
  # 実行結果　それ以外の数値が入力された場合
  # 　　　　　　数字を入力してください。
  # 　　　　　　2
